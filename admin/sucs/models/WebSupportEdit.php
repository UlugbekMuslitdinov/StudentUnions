    {
        global $Language;

        // Check if validation required
        if (!Config("SERVER_VALIDATE")) {
            return true;
        }
        if ($this->id->Required) {
            if (!$this->id->IsDetailKey && EmptyValue($this->id->FormValue)) {
                $this->id->addErrorMessage(str_replace("%s", $this->id->caption(), $this->id->RequiredErrorMessage));
            }
        }
        if ($this->name_first->Required) {
            if (!$this->name_first->IsDetailKey && EmptyValue($this->name_first->FormValue)) {
                $this->name_first->addErrorMessage(str_replace("%s", $this->name_first->caption(), $this->name_first->RequiredErrorMessage));
