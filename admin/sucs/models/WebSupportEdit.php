        }
        if ($this->name_last->Required) {
            if (!$this->name_last->IsDetailKey && EmptyValue($this->name_last->FormValue)) {
                $this->name_last->addErrorMessage(str_replace("%s", $this->name_last->caption(), $this->name_last->RequiredErrorMessage));
