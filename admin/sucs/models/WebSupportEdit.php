        }
        if ($this->_email->Required) {
            if (!$this->_email->IsDetailKey && EmptyValue($this->_email->FormValue)) {
                $this->_email->addErrorMessage(str_replace("%s", $this->_email->caption(), $this->_email->RequiredErrorMessage));
            }
        }
        if ($this->phone->Required) {
            if (!$this->phone->IsDetailKey && EmptyValue($this->phone->FormValue)) {
                $this->phone->addErrorMessage(str_replace("%s", $this->phone->caption(), $this->phone->RequiredErrorMessage));
            }
        }
        if ($this->url->Required) {
            if (!$this->url->IsDetailKey && EmptyValue($this->url->FormValue)) {
                $this->url->addErrorMessage(str_replace("%s", $this->url->caption(), $this->url->RequiredErrorMessage));
            }
        }
        if ($this->web_support->Required) {
            if (!$this->web_support->IsDetailKey && EmptyValue($this->web_support->FormValue)) {
                $this->web_support->addErrorMessage(str_replace("%s", $this->web_support->caption(), $this->web_support->RequiredErrorMessage));
            }
        }
        if ($this->web_support_title->Required) {
            if (!$this->web_support_title->IsDetailKey && EmptyValue($this->web_support_title->FormValue)) {
                $this->web_support_title->addErrorMessage(str_replace("%s", $this->web_support_title->caption(), $this->web_support_title->RequiredErrorMessage));
            }
        }
        if ($this->urgent->Required) {
            if (!$this->urgent->IsDetailKey && EmptyValue($this->urgent->FormValue)) {
                $this->urgent->addErrorMessage(str_replace("%s", $this->urgent->caption(), $this->urgent->RequiredErrorMessage));
            }
