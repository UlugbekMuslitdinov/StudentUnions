<span style="color:#59ad40; font-size:12px">Recycling Basics at the Unions</span> <br /><br />

<img src="images/recycle1_2plastic.png" style="float:left; padding-right:5px;" />
To recycle plastic anywhere in Tucson, you
must first decipher what type of plastic you have. Virtually all plastic is labeled with a number,
always located inside a recycling symbol triangle. This refers to the density, type of material
used, and number of times it has already been recycled (though a number 2 plastic has not
necessarily already been recycled twice). Tucson currently only accepts Number 1 and
Number 2 plastics. Number 1 plastics, or PETE plastics, are containers like soda and water
bottles. Number 2 plastics, or HDPE plastics, include containers like milk, detergent, and oil
bottles. Be responsible and always check the type of plastic before throwing it in the recycling
bin. Unfortunately, the lids don't count, so remove them, and the liquid in the bottles are best emptied first. 
