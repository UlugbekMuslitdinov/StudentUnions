	<h2 class="expand" >Guest Policy</h2>
	<div class="collapse">
		<p>
			The sponsoring organization is responsible for the behavior of its members and guests. All guests must be at least 18 years of age 
			with valid photo identification. <br />
		</p>
		<ol style="line-height: 1.25em;">
			<li>Each student entering the dance is allowed one (1) guest who must sign a guest list. Only one guest per organization member applies to all dances.</li>
			<li>The organization is to submit a pre-printed member and guest list with the first and last names. The guest list must be submitted to the Event Services Office seventy-two (72) hours prior to the dance.</li>
			<li>Guests that are neither on the pre-printed guest list or admitted with an organization member will not be admitted to the dance.</li> 
