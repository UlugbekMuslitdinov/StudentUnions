	<h2 class="expand" >Security</h2>
	<div class="collapse">
		<p>
			Security is required for all student dances. Security requirements are determined in conjunction with UAPD (University of Arizona Police Department) 
			and UA Risk Management. Considerations include the dance day, time and place, expected attendance, expected number of guests not on the pre-printed 
			guest list, the past history of the event, the type of event itself and the sponsoring organization. UAPD will provide security for all dances in the 
			Student Union Memorial Center. <u>The client or sponsoring organization is responsible for contacting UAPD and making all necessary security arrangements.</u> 
			A request for security personnel should be made at least 3 weeks before the event to ensure UAPD has time to staff the event. Please contact Melissa Valenzuela  
			at 626-6728 for information on cost or to schedule police officers. University Risk Management may require additional security and this will be discussed 
			with Client as far ahead as possible. 
		</p>

		<p>
			<b>UAPD contact is Melissa Valenzuela  at 626-6728.</b> UAPD will decide how many officers are necessary for the dance and all arrangements including payment 
			are to be made directly with the UAPD contact. In addition, the following will also apply:
		</p>

		<ol style="line-height: 1.25em;">
			<li>The sponsoring organization must identify one (1) individual member who will specifically be charged with the responsibility for assisting with security. This individual shall be stationed at the door throughout the dance to take an accurate head count.</li>
			<li>The sponsoring organization/representative must also check identification, collect admission fees, and communicate with security and Student Union staff. These individuals must check with the Union's Operations Manager before the doors open for the dance.</li>
			<li>All dance participants are subject to pat down. Purses/fanny packs may be searched. No large bags, (i.e., backpacks), are permitted.</li>
			<li>Participants who choose to leave the designated dance area must repeat the complete security screening process and pay any admission fees again.</li>
			<li>If anticipated attendance and the security of the facility necessitate, the University Police Department and/or Arizona Student Unions reserve the right to require the sponsoring organization to hire additional security. In such cases, every effort will be made to discuss these concerns with the sponsoring organization as far in advance as possible.</li>
			<li>The Arizona Student Unions and/or security reserve the right to refuse admission to anyone.</li>
			<li>The Arizona Student Unions reserves the right to close the dance or end the event at any time if they feel that canceling the dance is in the best interest and safety of the dance guests and/or Student Union Memorial Center staff.</li> 
		</ol>
