	<h2 class="expand" >Facility Fees</h2>
	<div class="collapse">
		<p> 
			Facility fees include room rental charges, cleaning fees, overtime labor, and in rare cases, damage charges.
		</p>
		<ol style="line-height: 1.25em;">
			<li>
				Room Rental - The following facilities are available for dances at the associated rates:
				The Student Union Memorial Center Grand Ballroom University rate: $800 /Non-University rate: beginning rate: $1500 to $3000 Please note that the 
				entire Grand ballroom needs to be rented when a dance is taking place (regardless of how many guests are expected to attend) to avoid any sound 
				conflicts with neighboring events. 1. Cleaning - There are minimum cleaning charges for all dances. Additional cleaning charges may be assessed 
				as needed to restore the facility to its original state. The minimum cleaning charge is $150.00.
			</li> 
			<li>
				Overtime Labor - There is a minimum overtime labor fee of $50/hour for every hour that the facility is not completely cleared after the building 
				closes, this includes increments of hours. Groups will be charged the full hourly rate regardless of whether they stay in the building for the
				full hour over or not. This includes DJ equipment and load-out. The charge goes up to $200/hour after 1am. It is important to schedule the end 
				time of the dance to assure there is sufficient time to load out for the event. 
			</li> 
			<li>
				Damage Charges - The sponsoring organization will be charged for any damages that occur during the dance.
				<ol style="list-style-type: lower-alpha; margin-top: 5px; margin-bottom: 5px;" >
					<li>
						A facility checklist will be completed prior to the start of the dance to document damage that is preexisting and will be reviewed and signed 
						by the Unions' staff member on duty and the sponsoring organization.
					</li>
				</ol>
			</li> 
			<li>
				At least seven (7) days prior to the event, the sponsoring organization is required to submit payment for the cleaning charge and room rental. Any 
				remaining balance is due after the event and must be paid within five (5) days of the dance. If the fees are not paid, all future reservations will 
				be canceled. Dances must be canceled seventy-two (72) hours in advance to avoid room, catering, and security charges.
			</li>  
		</ol>
