<p style="margin-top: 2px;">
	Recognizing the need for safe social activities, the University of Arizona Student Unions developed these general guidelines for dances held in 
	Arizona Student Unions' facilities. Please review this policy prior to submitting your request for facility use. 
</p>

<p>
	Please note that this dance policy is primarily used for University of Arizona recognized clubs and organizations holding dances within the 
	Student Union. Private groups, University departments or non-University organizations hosting dances follow approximately the same policies 
	with some exceptions. Please speak with an Event planner directly if your event falls in to the category of a private event or non-University 
	dance so all the details of your event can be discussed in advance. Failure to meet all the Student Union policies and procedures for events 
	with dances will result in cancellation of the event. 
</p>

