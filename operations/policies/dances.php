	<h2 class="expand" >Liability Insurance</h2>
	<div class="collapse">
		<p>
			If permission to conduct an event is granted, it is understood that the signing individual and/or the sponsoring organization, will take full 
			responsibility for injury to any person or property caused by the organization or members thereof and will be financially responsible for both 
			damages and expenses resulting there from. It is also understood that the signing individual and/or the sponsoring organization assume all risks 
			for injury or loss to the property of members or guests of the sponsoring organization and that approval of the event does not provide any coverage 
			by University insurance. Evidence of insurance, by issuance of a certificate naming the Arizona Board of Regents on behalf of the University of 
			Arizona as an additional named insured, for commercial general liability insurance in a single limit amount of $1,000,000, or more as appropriate 
			to the risk of the event as required by the Department of Risk Management, must be provided. The certificate shall clearly establish that the 
