<p>
	<b>The purpose of this policy is to outline the guidelines for use of designated rooms or office space within the Student Union Memorial Center (SUMC) by 
		a department or organization.</b>
</p>
<ol style="margin-left:20px;">
	<li>
		Use of space within the SUMC is a privilege which will be extended to certain UA departments, governing councils and/or recognized student organizations 
		on an annual basis.
	</li>
	<li>
		The Arizona Student Unions reserves the right to terminate an organization's use of space at the end of the designated occupancy period or due to a violation 
		of the terms of use for the space. They also reserve the right to refuse to accommodate any organization's request for space.
	</li>
</ol>
<p>
	<b><u>Term of Use</u>:</b>
</p>
<ol style="margin-left:20px;">
	<li>
		The Arizona Student Unions shall determine the length of use for each space. Typical length of use will follow an academic year calendar. Each space occupancy 
		agreement will be reviewed on an annual basis with each group submitting a new request to utilize the space for the next academic year.&nbsp; Renewals are not 
		guaranteed.
	</li>
	<li>
		Requests for space occupancy and renewal of space will be due April 1 of each year and will be reviewed by the Arizona Student Unions&rsquo; Director or 
		designee.<i> </i>Decisions will be made by May 1 of each year.&nbsp; Spaces to be vacated should be done so by June 1 so that new groups can move in beginning 
		June 15.&nbsp;
	</li>
</ol>
<p>
	<b><u>Guidelines for Use of Space</u>:</b>
</p>
<ol style="margin-left:20px;">
	<li>
		The Office space is to be utilized for legitimate activities, meetings, gatherings, and functions consistent with the recognized purpose of the organization and 
		the curricular and co curricular mission of the University, and shall be in compliance with all terms of this Agreement and University policies (i.e., Student 
		Union Memorial Center policies, Student Code of Conduct and Arizona laws).
	</li>
	<li>
		It is expected that all groups and guests of the organization utilizing the space maintain an attitude of respect and civility and respond in a professional manner 
		to SUMC staff requests.
	</li>
	<li>
		Sleeping, overnight stays, or using the office space as a place of residence in any way by any individual is absolutely prohibited and will automatically subject 
		guilty persons to University disciplinary action and may result in loss of office space privileges for the organization.
	</li>
	<li>
		Unless the Assistant Director for Student Unions Facilities Management grants special written permission, the hours of use for the designate space must follow the 
		business hours during which the Student Union Memorial Center is open. When the SUMC is closed during holiday breaks there will be no access to the designated office space.
	</li>
	<li>
		The designated office space must be kept clean and organized.&nbsp; Any shared common space should be kept clean of trash and other items at all times. Any changes 
		in office appearance must be submitted to and approved by the Assistant Director for Student Unions Facilities Management. SUMC staff must complete all work. This 
		includes changes of paint, hanging items on the wall etc.
	</li>
	<li>
		All furniture assigned to the office/space must remain in the office. All common area furniture must remain in the common area space and should not be placed in offices.
	</li>
	<li>
		Any general maintenance concerns should be reported to the SUMC Information Desk (621-7755) or through our online <a href="http://sutech.union.arizona.edu/" 
		target="_blank">work order request system</a>.
	</li>
	<li>
		All designated offices should follow the SUMC posting policy which includes only posting flyers in designated office spaces with approved substances 
		(see Assistant Director for Student Unions Facilities Management for list). Any posting of organization information outside of your designated space must follow the 
		SUMC Posting Policy which can be found at: <a href="/operations/policies/"
		onclick="window.open(this.href); return false;"
		onkeypress="window.open(this.href); return false;" >union.arizona.edu/operations/policies</a>.
	</li>
	<li>
		Any violations of these guidelines could result in a termination of the organization's privilege to utilize the designated space.
	</li>
</ol>
<p>
