<h2 style="margin-top:15px;">Reservations And Cancellations</h2>
<p style="margin-top: -8px;">Space is limited. It is recommended that you reserve your space in advance. Reservations are accepted for the current UA fiscal year only (exception being June). Full Payment is due at time of reservation. Reservations cancelled 7 calendar days prior to reserved display date may receive a full refund and are not subject to penalty. Reservations cancelled after this time are subject to a $25 cancellation fee, and reservations cancelled once toppers have been printed will incur an additional $45 reprinting fee.</p>

<h2 style="margin-top:15px;">Distribution</h2>
<p style="margin-top: -8px;">Fast Copy will distribute the Table Toppers, setting them out on tables in the Student Union Memorial Center and in the Park Student Union on Mondays by 10:00 a.m. There are approximately 460 tables throughout the two buildings, and a Table Topper will be placed on each. Leftover toppers (approx 40) will be used to replenish empty tables throughout the week. Toppers will be removed Sunday night by Student Union Event Services staff.</p>

<h2 style="margin-top:15px;">Consecutive Weeks</h2>
<p style="margin-top: -8px;">Customers wishing to advertise on consecutive weeks may do so. Each week of advertising is a separate opportunity to reach students, and is therefore charged separately and in full.</p>

<h2 style="margin-top:15px;">Specialty Scheduling</h2>
<p style="margin-top: -8px;">Customers wishing to advertise during time periods other than the standard Monday through Sunday week, should inquire with Fast Copy about specialty scheduling.</p>

<h2 style="margin-top:15px;">Waiting List</h2>
<p style="margin-top: -8px;">During any given week, a maximum of three (3) Table Topper versions will be displayed on tables. If advertising space during a specific week is booked, customers have the option to sign onto a waiting list. If an order is cancelled, priority will be given to the first customer on the waiting list.</p>

<h2 style="margin-top:15px;">Artwork</h2>
<p style="margin-top: -8px;">Customers must provide a printed sample and a PDF file of the Table Topper artwork. We recommend providing artwork early so that changes can be made prior to final printing. <em><strong>Final, signed artwork is due to Fast Copy by 11a.m. on the Thursday prior to the display date.</strong></em> Artwork submitted late will delay the process, resulting in toppers being put on display late, incurring potential fees or administrative cancellation. Customers must supply their own artwork.</p>
<p style="font-size: 1.2em;">Graphic Design services are available at Fast Design, (520) 621-5305.</p>

