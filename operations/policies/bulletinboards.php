	held responsible for any damage incurred upon removal.
</p>
<ol style="margin-left:20px;">
	<li>
		All posters/flyers must be approved at the Student Union Memorial Center Information Desk, and must be stamped, dated and hung by the Information Desk staff.
	</li>
	<br />
	<li>
		All posters/flyers must be sponsored by a recognized student organization or University department and the name of the sponsoring organization/department must 
		be clearly printed/displayed on the poster/flyer.
	</li>
	<br />
	<li>
		Posters/flyers can only be posted on the approved bulletin boards located within the Student Union Memorial Center.
	</li>
	<br />
	<li>
		The Arizona Student Unions reserve the right to dispose of any unauthorized posters/flyers.
	</li>
	<br />
	<li>
		Posters/flyers may remain on the boards for up to fourteen (14) days or the day after event, whichever comes first.
	</li>
	<br />
	<li>
		Posters/flyers will be displayed on a first come first serve basis.
	</li>
	<br />
	<li>
		Only one poster per club, organization, event and/or department may be posted at any given time.
	</li>
	<br />
	<li>
		Recommented poster size is maximum 8 1/2" x 11". <a href="/fastcopy/index.php">Fast Copy</a>, located on the main floor of the SUMC provides competitively 
		priced service for design, copy or printing posters and flyers.
	</li>
	<br />
	<li>
		Posters/flyers may be bi-lingual as long as there is a comparable English translation on the flyer.
	</li>
	<br />
	<li>
		Posters/flyers advertising alcohol related events are not allowed and will not be approved.
	</li>
	<br />
	<li>
		Posters/flyers advertising rooms/apartments/houses for rent are not allowed and will not be posted-regardless of sponsorship. Contact
		<a href="http://offcampus.arizona.edu/">Off-Campus Housing</a> for approved advertising methods.
	</li>
	<br />
	<li>
		No outside vendors, companies, organizations or agencies may post flyers or posters on the bulletin boards unless they have scheduled the use of 
		facilities within the Arizona Student Unions through the Event Services Office.
	</li>
</ol>
