            <font style="font-size:12px; color:#333333;">The University of Arizona’s Comedy Corner, with the University Activities Board and the Center for Student Involvement and Leadership, is proud to present the Tucson community with the 18th annual Southwest Intergalactic Comedy Kermis, or S.I.C.K. Festival.  S.I.C.K Festival is a college comedy festival that began in 1992 and features comedy troupes from the southwest United States and a headline comedian or troupe.  Past headlining acts for S.I.C.K Fest include David Cross, Lewis Black, Demetri Martin, and The Upright Citizens Brigade.  The headlining act this year will be standup comedian Craig Robinson (Hot Tub Time Machine, The Office) who will be performing on Saturday, May 1st at 8:00 p.m.
            </font>
            <font style="font-size:12px; color:#333333;">
