<h2>JOB SUMMARY</h2>
<p>Under direction of the Executive Director, Arizona Student Unions, the Sr. Associate Director – Operations and Director of Park Student Union will provide leadership and professional expertise in the planning and implementation of Unions occurrences and functions; will work with the Director, staff, the unions’ retail operations and services, and other members of the campus community to collaboratively plan high-quality events and functions that will attract people to the unions.  S/he will help to identify and facilitate discussions and decisions about issues affecting planned events and will ensure that high standards and campus- wide priorities are reflected in the planning and presentation of events.  The incumbent will interact with faculty, staff, and students; donors to the unions; visiting dignitaries; community members; etc.  In addition, s/he has oversight of the Park Student Union, the Assistant Director for Facilities, and the Assistant Director for Event Services, AV and Catering, the Assistant Director for Wildcat Concessions and the Student Engagement Coordinator.  Responsible for providing intermediate and long-term direction to a multi-unit department and staff comprised of Student Union Building Management, Facilities Maintenance, Safety/Security, Capital Inventory, Custodial, Hospitality and the Loading Dock.  The Sr. Associate Director is responsible for guiding and leading efforts to identify services and programs that best meet the needs of the university community.</p>

