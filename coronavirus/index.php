            <h2 class="sans mt-0">
                Arizona Dining SMART
            </h2>

            <h3><b>Indoor Dining</b></h3>
            <ul>
                <li>Grand Ballroom</li>
                <li>Kachina Lounge</li>
                <li>Pangea</li>
                <li>Global Center Dining Hall</li>
                <p>Seating inside the SUMC Food Court and other Arizona Student Unions restaurants have been removed for your safety and health. 
                While eating indoors, please use this space for a limited time and practice social distancing at 6 feet apart.</p>
            </ul>

            <h3><b>Outdoor Dining - Large Tents</b></h3>
            <ul>
                <p>Seting is available at outdoor tents across campus</p>
                <li>Admin Building</li>
                <li>Kachina Balcony</li>
                <li>Global Center Lawn</li>
                <li>Honors Village Interior Courtyard</li>
                <li>UArizona Mall</li>
                <p>While eating outdoors, please use this space for a limited time and practice social distancing at 6 feet apart.</p>
            </ul>

            <p>
                In light of COVID-19, Arizona Dining has developed a cohesive plan to help reduce risk. Our plan is called Arizona Dining SMART. 
            </p>

            <p>
                SMART is an acronym which stands for:<br/>
                <b style="font-weight: 600;">S</b> – Sanitize and clean<br/>
                <b style="font-weight: 600;">M</b> – Maintain social distancing<br/>
                <b style="font-weight: 600;">A</b> – Accommodate dining offerings<br/>
                <b style="font-weight: 600;">R</b> – Require employees follow COVID-19 guidelines<br/>
                <b style="font-weight: 600;">T</b> – Tech driven in approach
            </p>

            <p>
                We will continue to update as guidelines from the university and/or county, state and national organizations are directed. Details for Arizona Dining SMART are below.
            </p>

            <h3><b style="font-weight: 600;">S</b> – <b style="font-weight: 600;">S</b>anitize and clean</h3>
            <ul>
                <li>High touch point areas including door handles or push plates, elevator buttons, stair rails, seatbacks, tables, faucets, guest microwaves and countertops are sanitized at increased frequency.</li>
                <li>Restroom refresh disinfectant cleaning every 2 hours.</li>
                <li>Customer facing hand sanitizer is available throughout locations.</li>
                <li>Restaurant units keep a cleaning log to record cleaning of counters, card readers and door handles or push plates. Cleaning must be completed and recorded every two hours.</li>
                <li>Back of the house areas will be cleaned at a higher frequency including employee work areas, employee entrances, locker rooms/bathrooms and offices.</li>
            </ul>

            <h3><b style="font-weight: 600;">M</b> – <b style="font-weight: 600;">M</b>aintain social distancing</h3>
            <ul>
                <li>Sticker decals are placed in each dining location to provide a 6 foot visual cue to guests</li>
                <li>Stanchions are utilized at all open restaurants.</li>
                <li>Limiting capacity in all dining locations.</li>
                <li>Plexi-glass barriers are placed at each register station.</li>
                <li>Offer only to-go food. Outdoor tents are set up to allow for open air seating with distancing.</li>
                <li>Card only is accepted including Meal Plan, CatCash, Credit or Debit. No cash.</li>
                <li>Signage at each entrance asking guests to STOP if they are experiencing COVID-19 symptoms and not enter the location. <br/>Additional information on the sign is provided for Campus Health testing as well as Pima County Health information for guests.</li>
            </ul>

            <h3><b style="font-weight: 600;">A</b> – <b style="font-weight: 600;">A</b>ccommodate dining offerings</h3>
            <ul>
                <li>Self-serve areas are modified to full-service style. Salad bars offer pre-packaged options only.</li>
                <li>Elimination of condiment bars with behind counter service of individually wrapped sugars, creamers, stirring rods, lids, ketchup, mustard and other condiments.</li>
                <li>New, unused disposable cups are only be used for beverages. No personal cups allowed and no refills using the same cup are allowed.</li>
                <li>Pre-wrapped straws and sealed flatware are offered.</li>
                <li>Produce and bakery items are pre-wrapped.</li>
                <li>All sampling has been paused.</li>
            </ul>

            <h3><b style="font-weight: 600;">R</b> – <b style="font-weight: 600;">R</b>equire employees follow COVID-19 guidelines</h3>
            <ul>
                <li>Handwashing with soap and water is required of all employees for at least 20 seconds every 30 minutes.</li>
                <li>Wellness & temperature checks are conducted for all employees as they arrive for work.</li>
                <li>Masks to cover mouth and nose are required for all staff working in dining locations.</li>
                <li>Gloves are required for all staff working in dining locations and are to be changed every thirty minutes with handwashing.</li>
                <li>Managers and person in charge are required to have ServSafe certification.</li>
                <li>All staff is trained on COVID-19 safety and sanitation protocols.</li>
            </ul>

            <h3><b style="font-weight: 600;">T</b> – <b style="font-weight: 600;">T</b>ech driven in approach</h3>
            <ul>
                <li>Offer GrubHub delivery or pick up.</li>
                <li>GrubHub kiosks available at several locations will have stylus pens that will be sanitized after use to reduce touch of screens.</li>
                <li>Customer facing card readers are implemented for Meal Plan and Credit/Debit cards to reduce contact.</li>
                <li>Website ordering for students in self-isolation awaiting COVID-19 test or results.</li>
                <li>Tech Vending options are added to allow contactless food ordering including salad, yogurt and hummus options along with ramen and pho options</li>
                <li>Smart Lockers are added for contactless mobile order pick up.</li>
                <li>Digital menus available to reduce contact of physical menus.</li>
            </ul>

