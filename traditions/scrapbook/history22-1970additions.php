<p>Registration week will usher in the grand opening of the new $3.5 million addition to the Student Union.</p>
<p><img height="225" width="214" src="history19additions.jpg" align="right">The 110,000 square foot addition to the twenty year old Union includes a movie theater, nightclub, dining room, art exhibition hall, three lounges and an enlarged Student Union Post Office.</p>
<p>The Art Exhibition Hall on the second floor features the work of Ed Hunter during the opening week.</p>
<p>The Hall of Fame, in which the Bobcats organization honors past distinguished graduates is located on the second floor as the Cactus Lounge.</p>
<p>Located on the basement level is a 225 seat nightclub called the Cellar, where a stage is set for various bands and music groups to perform. Performances at the club are free but a 75 cent admission may be charged for more notable bands.</p>
<p>Louie&#146;s Lower Level has been expanded and remodeled. A new hallway leads past the palace of sweets and up to the new Post office. The new post office allowed for the expanding of the games room, which added three pool tables and a ping pong table. Eventually, the games room will be moved underneath the Gallagher Theater to make room for a new student activities area.</p>
<p>The 630 seat Gallagher Theater named for Edward J. Gallagher who contributed films and artwork to the University, is located on the east end of the second floor.</p>
