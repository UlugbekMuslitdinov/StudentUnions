			<p>Bell from the <i>U.S.S. Arizona</i>, its plaque reads:</p>
			<p><b>THE PRESERVATION OF THE U.S.S. ARIZONA BELL IN THIS TOWER IS DEDICATED TO THE MEMORY OF THE MEN WHO LOST THEIR LIVES ON THE BATTLESHIP ARIZONA IN THE ATTACK ON PEARL HARBOR ON DECEMBER 7, 1941.</b></p>
			<p>This bell is one of the two original bells salvaged from the U.S.S. ARIZONA in 1941. In 1944, Wilber L. Bill Bowers, UA Class of 1927, discovered this bell about to be melted down at the Puget Sound Naval Yard in Bremerton, Washington. Bowers saved the bell from destruction and was instrumental in acquiring the bell for the University of Arizona shortly after World War II.</p>
			<p>The bell arrived on campus in July 1946. On November 17, 1951 the bell was rung for the first time in the clock tower of the then-new Memorial Student Union Building. The bell was rung on special occasions for the next 50 years until that clock tower and Student Union were razed to make way for the present day facility completed in 2002.</p>
			<p>The bell was installed in this clock tower on August 16, 2002. Bill Bowers, at the age of 99, was given the honor of ringing the bell for the first time in its new belfry on September 11, 2002. This bell is to be rung seven times on the third Wednesday of every month at 12:07 p.m. to honor the achievements of the University of Arizona and its community. It is traditionally rung by the Student Body President on the Sunday before Pearl Harbor Day, on the Student Union&#146;s birthday (November 18), and after Wildcat athletic victories (over any team except other Arizona schools).</p>
			<p>Effective Wednesday, April 16, 2003, the USS Arizona Bell will be rung for significant University achievements on the third Wednesday each month. This includes academic achievements and awards for which a University of Arizona student, faculty or staff has received recognition in the previous thirty days. The USS Arizona Bell will be rung 7 times at 12:07pm in recognition of these outstanding achievements. </p>
			<p>The other original bell is on display at the U.S.S. Arizona Memorial in Pearl Harbor, Hawaii.</p>
