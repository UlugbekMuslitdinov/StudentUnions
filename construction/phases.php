<h3>Phase II</h3>
<p>(15-18 months, expected completion December 2002)</p>
<p><img height="171" width="108" src="phasesModelpic2.jpg" align="left">In Phase II, the middle and western sides of the old building are being demolished and rebuilt with new ballrooms, a new theater/multi-purpose auditorium, expanded food court, a highly enhanced USS Arizona memorial lounge, career services, student government offices, and spaces to meet or hang out. The new Student Union/UA Bookstore will once again be the heartbeat of the campus.</p>
