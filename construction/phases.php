<h3>Phase I</h3>
<p>(16&#150;18 months, COMPLETED!)</p>
<p><img height="163" width="108" src="phasesModelpic.jpg" align="right" alt="model picture">During Phase I, the east end of the Union was torn down, including Gallagher theater, Sam's Place, the Cactus lounge, the Union Club and meeting rooms. The rest of the Union continued to function as usual: the bookstore was untouched, all the restaurants were also left open, Sam's Place was relocated to the 1st level and Gallagher Theater was moved temporarily to other facilities on campus.</p>
<p>On the east side of the Union, new restaurants have been built with indoor/outdoor seating. On the northwest side, a new bookstore is now finished, along with an underground loading dock. Now that Phase I is completed students are able to eat in the new restaurants; buy their books at the new bookstore, and continue to receive services as always.</p>
