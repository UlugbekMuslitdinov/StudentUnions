<h1>Opening An Off-Campus Bank Account</h1>
<p><b>Why</b>: An organization that raises money separate from the funds appropriated to it by ASUA or a University department will need to have an off-campus bank account to deposit and draw funds from.</p>
<p><b>Where</b>: Many student organizations choose to use Vantage Credit Union because of their location and familiarity with University student organization checking accounts. You may choose any bank you feel comfortable with.</p>
<p>If you decide to open an account with Vantage, you must obtain a letter of recognition from the Center for Student Involvement &amp; Leadership.</p>
<p><b>How</b>: To open an account you will need either a student&#146;s social security number (not a good idea) or a tax ID number. Apply for a tax ID number using form SS-4. Forms are available <a href="http://sa1.www4.irs.gov/modiein/individual/index.jsp">here!</a></p>
<p><b>Restrictions</b>: You may not use &#147;The University of Arizona&#148; or &#147;UA&#148; as part of the organization name on your account. University funds, including ASUA monies may not be in an off-campus account.</p>
