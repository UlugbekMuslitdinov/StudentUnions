<p><b>Sponsoring Vendors</b><br>
	Sponsoring Commercial Activity on the mall is a benefit to being a recognized student organization at the University of Arizona. Sponsorship, in one word, is fundraising. Bringing an approved vendor to the mall allows your group to earn money. Examples of vendors include, but are not limited to, jewelry, clothing, credit cards and hand made crafts. Vendors not only pay a student organization for sponsorship, they also pay a mall use fee. Student organizations can earn an average of $50&#150;$100 per day.</p>
<p>Step by Step to sponsoring a Vendor on the Mall</p>
<ol>
	<li>The Student Organization approaches or solicits vendors to sponsor.
	<li>Bring the name of the vendor and space request to <a href="/mall/index.php">Campus Use & Mall Scheduling</a>.
	<li>Wait for approval from mall scheduling.
	<li>Follow the mall sponsorship policy (see the Policy section of the <a href="/csil/clubs/handbook.php">Handbook</a>)
</ol>
<p>AND/OR</p>
<ol>
	<li>Ask to be added to the &#147;sponsorship&#148; list in Campus Use & Mall Scheduling.
	<li>Wait for a call from the Campus Use & Mall Scheduling Office.
	<li>Choose whether or not you are interested in the offer the vendor presents.
	<li>Work with Diane Newman in the Campus Use & Mall Scheduling Office to complete appropriate paperwork.
</ol>
<p>For more information please stop by the Campus Use & Mall Scheduling Office in MSU 404 or call 626-2630.</p>
<p><b>Spring Fling</b><br>
	Spring Fling is the largest student run carnival in the nation! Since 1974, our vision has been to help clubs and organizations at the University raise funds for the programs and services they provide to both the community and to the University.</p>
<p>Today, we are proud to announce that clubs and organizations raise over $86,000 a year through their efforts at Spring Fling!</p>
<p>How can your organization get involved?<br>
	&#149;	Contact the Spring Fling office during fall semester at 621-5610, meetings begin in late November.</p>
<p>How does your club raise funds at Spring Fling?<br>
	&#149;	Each club, whether alone or paired with another club, sells food, drinks, or games at a booth on the Spring Fling field.</p>
<p>What are the expectations of the clubs and organizations?<br>
	&#149;	There should be one or more representatives from your club who will be responsible for finding workers in your club, attending meetings, filling out paperwork, and maintaining a positive attitude among workers!</p>
