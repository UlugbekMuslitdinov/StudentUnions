<p><b>The Student/Faculty Interaction Program</b><br>
	Your faculty advisor will need to help you secure this funding. Another option would be for your organization to ask a favorite professor to work with your group as a sponsor.</p>
<p>WHAT: The Student/Faculty Interaction Program was established over ten years ago in response to research that shows the more contact student have with faculty, the more likely they will persist in school and graduate. The program provides funds to faculty for various activities with students outside the classroom allowing participants to interact in a more social setting. In previous years, faculty members have hosted trips, excursions, &#147;get togethers&#148; at faculty homes, brown bag lunches, exam review sessions, night at the opera, field trips, and a wealth of other activities.</p>
<p>HOW: Applications can be picked up and returned to the Dean of Students Office, Old Main 203. All applications will be evaluated by the following criteria:</p>
<ul>
	<li>The quality of interaction likely to occur between students and faculty as a result of the program. Applications will be reviewed and evaluated on their intellectual and social merits, with neither taking precedence.<br>
		<br>
	
	
	<li>Inclusion of, but not limited to, new students to the University. The following priorities will be followed:
</ul>
<ol>
	<ol>
		<li>Freshman and new transfers have top priority
		<li>Underclassman over upperclassman
		<li>Undergraduate over graduate.
	</ol>
</ol>
<ul>
	<li>The reasonableness of the amount of funding requested. &#147;Reasonableness&#148; is defined as no more than $7.00/per participant.<br>
		<br>
	
	
	<li>Faculty and/or graduate teaching assistants with grading responsibilities must initiate or sponsor the proposals. Non-academic units may submit proposals with a faculty co-sponsor. Priority will be given to professors who teach introductory courses.
</ul>
<p>DEADLINES:  Because the demand for the monies has exceeded the total amount allocated for the program, application deadlines have been established to maximize the total number of grants awarded. The annual appropriation of funding will be split across five application deadlines. Call the Dean of Students Office, 621-7059 or the Center for Student Involvement &amp; Leadership 621-8046 to get a list of the application deadlines.</p>
<p>DETAILS: In preparing your application, thoroughly plan your event and contact all vendors to be certain your plans are acceptable to them. Our office can advise you of steps to take, but will not make any arrangements for your event. For further questions, call 621-7059.</p>
