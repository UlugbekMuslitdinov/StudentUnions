3.     The 50/50 meal discount card can only be used once per day.<br />
4.   You must have your name tag on and it must be visible.<br />
5.        The 50/50 card is nontransferable and can only be used by the employee to whom it was issued.<br />
6. Only items prepared within the Unions qualify for the discount. Resale items (prepackaged chips, gum, canned sodas, candy, etc.) will be charged to you at full cost. If in doubt ask your supervisor.<br />
7.        The 50/50 card can be used in the following locations but you will not receive a 50% discount: Brueggers' Bagels, Burger King, Canyon Caf&eacute;, Chick-fil-A, Coffee Carts, Oy Vey Caf&eacute;, Panda Express, Papa John's and Redington Restaurant. <br />
8.       The 50/50 card is valid at all Union restaurants (except as noted in Rule 7), and is only valid at Core after 1:30pm.<br />
9. The 50/50 card cannot be used in conjunction with other discount coupons.<br />
10.    The 50/50 card is the property of the Arizona Student Unions and must be surrendered upon demand and/or at the time you leave employment.<br />
11.    Balances of $5 or less will not be refunded, balances in excess of this will be reimbursed via Check Request.<br />
12.        If card is found to be improperly used, privileges will be suspended.</p>
